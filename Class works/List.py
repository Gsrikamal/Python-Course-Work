# Creating a list
fruits = ["apple", "banana", "mango"]
print("Fruits:", fruits)

# Nested list
nested = [1, 2, ["a", "b", "c"]]
print("Nested list:", nested)

# Adding items
fruits.append("orange")  # add at end
fruits.insert(1, "grapes")  # add at index
print("Updated Fruits:", fruits)

# Accessing items
print("First item:", fruits[0])
print("Last item:", fruits[-1])
